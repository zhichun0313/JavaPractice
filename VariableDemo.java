public class VariableDemo {
    public static void main(String[] args) {
        int age = 18;
        char gender = 'M';
        System.out.println("Age:");
        System.out.println(age);
        System.out.println("Gender:");
        System.out.println(gender);
        age = 20;
        gender = 'F';
        System.out.println("Age:");
        System.out.println(age);
        System.out.println("Gender:");
        System.out.println(gender);

    }
}
